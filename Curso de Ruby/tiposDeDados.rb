    inteiro.class; #para verificar o time de dado

#FLOAT (numeros decimais)
    pontoFlutuante = 4.6;
    pontoFlutuante.class;

#STRING (conjuntos de letras, simbolos ou numeros)
    string = 'oi';
    string.class; #para verificar o time de dado

#BOOLEAN (verificar se é verdadeiro (true) ou falso(false))
    verdadeiro = true;
    verdadeiro.class; #para verificar o time de dado (classTrue)
    falso = false;
    falso.class; #para verificar o time de dado (classFalse)

#ARRAY (armazena uma lista em uma unica variavel)
    array = [1, 2, 3, 4, 5];
    array.class; #para verificar o time de dado

